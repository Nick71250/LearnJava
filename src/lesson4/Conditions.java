package lesson4;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 4;

        if (x > y) {
            System.out.println("X больше Y");
        } else if (x == y) {
            System.out.println("X равен Y");
        } else {
            System.out.println("X меньше Y");
        }
    }
}
